import optuna
import numpy as np
import pandas as pd
from sklearn.model_selection import KFold
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, roc_auc_score

X_train = pd.read_csv('/content/X_train_final.csv').values
y_train = pd.read_csv('/content/y_train_final.csv').values.astype(np.float32).flatten()

X_val = pd.read_csv('/content/X_val_processed.csv').values
y_val = pd.read_csv('/content/y_val_processed.csv').values.astype(np.float32).flatten()

X_combined_for_cv = np.concatenate((X_train, X_val), axis=0)
y_combined_for_cv = np.concatenate((y_train, y_val), axis=0)

def objective_lr(trial):
    C = trial.suggest_float('C', 1e-4, 1e2, log=True)
    penalty = trial.suggest_categorical('penalty', ['l1', 'l2'])
    solver = trial.suggest_categorical('solver', ['liblinear', 'saga'])

    if penalty == 'l1' and solver not in ['liblinear', 'saga']:
        raise optuna.exceptions.TrialPruned()
    if penalty == 'l2' and solver not in ['lbfgs', 'liblinear', 'newton-cg', 'sag', 'saga']:
        pass

    lr_model = LogisticRegression(C=C, penalty=penalty, solver=solver, max_iter=1000, random_state=42)

    lr_model.fit(X_train, y_train)
    y_pred_proba_val = lr_model.predict_proba(X_val)[:, 1]

    if len(np.unique(y_val)) > 1:
        val_auc = roc_auc_score(y_val, y_pred_proba_val)
        val_loss = 1.0 - val_auc
    else:
        return float('inf')

    return val_loss

print("\n--- Starting Optuna Optimization Study for Logistic Regression Baseline (Minimizing 1-AUC) ---")
study_lr = optuna.create_study(direction='minimize', study_name='LR_Optimization',
                              pruner=optuna.pruners.MedianPruner(n_startup_trials=10, n_warmup_steps=20))
study_lr.optimize(objective_lr, n_trials=200, show_progress_bar=True)

print("\n--- Logistic Regression Optimization Finished ---")
print(f"Number of finished trials: {len(study_lr.trials)}")
print(f"Best trial value (Validation 1-AUC): {study_lr.best_trial.value:.4f}")
print(f"Corresponding Best Validation AUC: {1 - study_lr.best_trial.value:.4f}")
print("Best Logistic Regression hyperparameters:")
best_params_lr = study_lr.best_trial.params
for key, value in best_params_lr.items():
    print(f"  {key}: {value}")

n_splits = 10
kf = KFold(n_splits=n_splits, shuffle=True, random_state=42)

lr_accuracy_scores = []
lr_auc_scores = []
lr_precision_scores = []
lr_recall_scores = []
lr_f1_scores = []

print(f"\n--- Starting {n_splits}-Fold Cross-Validation for Optimized Logistic Regression Baseline ---")

for fold, (train_index, val_index) in enumerate(kf.split(X_combined_for_cv, y_combined_for_cv)):
    print(f"\n--- Fold {fold+1}/{n_splits} ---")
    X_train_fold, X_val_fold = X_combined_for_cv[train_index], X_combined_for_cv[val_index]
    y_train_fold, y_val_fold = y_combined_for_cv[train_index], y_combined_for_cv[val_index]

    single_class_in_val_fold = (len(np.unique(y_val_fold)) <= 1)
    if single_class_in_val_fold:
        print(f"WARNING: Fold {fold+1} validation set contains only one class. AUC will be NaN.")

    lr_model_cv = LogisticRegression(
        C=best_params_lr['C'],
        penalty=best_params_lr['penalty'],
        solver=best_params_lr['solver'],
        max_iter=1000,
        random_state=42
    )

    lr_model_cv.fit(X_train_fold, y_train_fold)
    y_pred_proba_lr = lr_model_cv.predict_proba(X_val_fold)[:, 1]
    y_pred_class_lr = lr_model_cv.predict(X_val_fold)

    lr_accuracy_scores.append(accuracy_score(y_val_fold, y_pred_class_lr))
    if not single_class_in_val_fold:
        lr_auc_scores.append(roc_auc_score(y_val_fold, y_pred_proba_lr))
    else:
        lr_auc_scores.append(np.nan)
    lr_precision_scores.append(precision_score(y_val_fold, y_pred_class_lr, zero_division=0))
    lr_recall_scores.append(recall_score(y_val_fold, y_pred_class_lr, zero_division=0))
    lr_f1_scores.append(f1_score(y_val_fold, y_pred_class_lr, zero_division=0))

    print(f"  Logistic Regression Baseline Fold {fold+1} Metrics:")
    print(f"    Accuracy: {lr_accuracy_scores[-1]:.4f}")
    print(f"    AUC: {lr_auc_scores[-1]:.4f}" if not np.isnan(lr_auc_scores[-1]) else "AUC: N/A")
    print(f"    Precision: {lr_precision_scores[-1]:.4f}")
    print(f"    Recall: {lr_recall_scores[-1]:.4f}")
    print(f"    F1-Score: {lr_f1_scores[-1]:.4f}")

print("--- Optimized Logistic Regression Baseline: 10-Fold Cross-Validation Results (Mean ± Standard Deviation) ---")
print(f"Accuracy: {np.nanmean(lr_accuracy_scores):.4f} ± {np.nanstd(lr_accuracy_scores):.4f}")
print(f"AUC: {np.nanmean(lr_auc_scores):.4f} ± {np.nanstd(lr_auc_scores):.4f}")
print(f"Precision: {np.nanmean(lr_precision_scores):.4f} ± {np.nanstd(lr_precision_scores):.4f}")
print(f"Recall: {np.nanmean(lr_recall_scores):.4f} ± {np.nanstd(lr_recall_scores):.4f}")
print(f"F1-Score: {np.nanmean(lr_f1_scores):.4f} ± {np.nanstd(lr_f1_scores):.4f}")

def bootstrap_confidence_interval(data, alpha=0.95, n_bootstraps=10000):
    if len(data) == 0 or np.all(np.isnan(data)):
        return (np.nan, np.nan)

    data = np.array(data)[~np.isnan(data)]
    if len(data) == 0:
        return (np.nan, np.nan)

    bootstrap_means = []
    for _ in range(n_bootstraps):
        sample = np.random.choice(data, size=len(data), replace=True)
        bootstrap_means.append(np.mean(sample))

    lower_percentile = (1 - alpha) / 2 * 100
    upper_percentile = (1 + alpha) / 2 * 100

    lower_bound = np.percentile(bootstrap_means, lower_percentile)
    upper_bound = np.percentile(bootstrap_means, upper_percentile)

    return (lower_bound, upper_bound)

print("\n--- Optimized Logistic Regression Baseline: 95% Confidence Intervals (from 10-Fold CV) ---")
ci_lr_accuracy = bootstrap_confidence_interval(lr_accuracy_scores)
ci_lr_auc = bootstrap_confidence_interval(lr_auc_scores)
ci_lr_precision = bootstrap_confidence_interval(lr_precision_scores)
ci_lr_recall = bootstrap_confidence_interval(lr_recall_scores)
ci_lr_f1 = bootstrap_confidence_interval(lr_f1_scores)

print(f"Accuracy CI: ({ci_lr_accuracy[0]:.4f}, {ci_lr_accuracy[1]:.4f})")
print(f"AUC CI: ({ci_lr_auc[0]:.4f}, {ci_lr_auc[1]:.4f})")
print(f"Precision CI: ({ci_lr_precision[0]:.4f}, {ci_lr_precision[1]:.4f})")
print(f"Recall CI: ({ci_lr_recall[0]:.4f}, {ci_lr_recall[1]:.4f})")
print(f"F1-Score CI: ({ci_lr_f1[0]:.4f}, {ci_lr_f1[1]:.4f})")