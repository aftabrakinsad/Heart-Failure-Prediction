import optuna
import numpy as np
import pandas as pd
from sklearn.model_selection import KFold
from sklearn.tree import DecisionTreeClassifier
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, roc_auc_score

X_train = pd.read_csv('/content/X_train_final.csv').values
y_train = pd.read_csv('/content/y_train_final.csv').values.astype(np.float32).flatten()

X_val = pd.read_csv('/content/X_val_processed.csv').values
y_val = pd.read_csv('/content/y_val_processed.csv').values.astype(np.float32).flatten()

X_combined_for_cv = np.concatenate((X_train, X_val), axis=0)
y_combined_for_cv = np.concatenate((y_train, y_val), axis=0)

def objective_dt(trial):
    max_depth = trial.suggest_int('max_depth', 3, 20)
    min_samples_split = trial.suggest_int('min_samples_split', 2, 20)
    min_samples_leaf = trial.suggest_int('min_samples_leaf', 1, 10)
    criterion = trial.suggest_categorical('criterion', ['gini', 'entropy'])

    dt_model = DecisionTreeClassifier(
        max_depth=max_depth,
        min_samples_split=min_samples_split,
        min_samples_leaf=min_samples_leaf,
        criterion=criterion,
        random_state=42
    )

    dt_model.fit(X_train, y_train)
    y_pred_proba_val = dt_model.predict_proba(X_val)[:, 1]

    if len(np.unique(y_val)) > 1:
        val_auc = roc_auc_score(y_val, y_pred_proba_val)
        val_loss = 1.0 - val_auc
    else:
        return float('inf')

    return val_loss

print("\n--- Starting Optuna Optimization Study for Decision Tree Baseline (Minimizing 1-AUC) ---")
study_dt = optuna.create_study(direction='minimize', study_name='DT_Optimization',
                              pruner=optuna.pruners.MedianPruner(n_startup_trials=5, n_warmup_steps=10))
study_dt.optimize(objective_dt, n_trials=200, show_progress_bar=True)

print("\n--- Decision Tree Optimization Finished ---")
print(f"Number of finished trials: {len(study_dt.trials)}")
print(f"Best trial value (Validation 1-AUC): {study_dt.best_trial.value:.4f}")
print(f"Corresponding Best Validation AUC: {1 - study_dt.best_trial.value:.4f}")
print("Best Decision Tree hyperparameters:")
best_params_dt = study_dt.best_trial.params
for key, value in best_params_dt.items():
    print(f"  {key}: {value}")

n_splits = 10
kf = KFold(n_splits=n_splits, shuffle=True, random_state=42)

dt_accuracy_scores = []
dt_auc_scores = []
dt_precision_scores = []
dt_recall_scores = []
dt_f1_scores = []

print(f"\n--- Starting {n_splits}-Fold Cross-Validation for Optimized Decision Tree Baseline ---")

for fold, (train_index, val_index) in enumerate(kf.split(X_combined_for_cv, y_combined_for_cv)):
    print(f"\n--- Fold {fold+1}/{n_splits} ---")
    X_train_fold, X_val_fold = X_combined_for_cv[train_index], X_combined_for_cv[val_index]
    y_train_fold, y_val_fold = y_combined_for_cv[train_index], y_combined_for_cv[val_index]

    single_class_in_val_fold = (len(np.unique(y_val_fold)) <= 1)
    if single_class_in_val_fold:
        print(f"WARNING: Fold {fold+1} validation set contains only one class. AUC will be NaN.")

    dt_model_cv = DecisionTreeClassifier(
        max_depth=best_params_dt['max_depth'],
        min_samples_split=best_params_dt['min_samples_split'],
        min_samples_leaf=best_params_dt['min_samples_leaf'],
        criterion=best_params_dt['criterion'],
        random_state=42
    )

    dt_model_cv.fit(X_train_fold, y_train_fold)
    y_pred_proba_dt = dt_model_cv.predict_proba(X_val_fold)[:, 1]
    y_pred_class_dt = dt_model_cv.predict(X_val_fold)

    dt_accuracy_scores.append(accuracy_score(y_val_fold, y_pred_class_dt))
    if not single_class_in_val_fold:
        dt_auc_scores.append(roc_auc_score(y_val_fold, y_pred_proba_dt))
    else:
        dt_auc_scores.append(np.nan)
    dt_precision_scores.append(precision_score(y_val_fold, y_pred_class_dt, zero_division=0))
    dt_recall_scores.append(recall_score(y_val_fold, y_pred_class_dt, zero_division=0))
    dt_f1_scores.append(f1_score(y_val_fold, y_pred_class_dt, zero_division=0))

    print(f"  Decision Tree Baseline Fold {fold+1} Metrics:")
    print(f"    Accuracy: {dt_accuracy_scores[-1]:.4f}")
    print(f"    AUC: {dt_auc_scores[-1]:.4f}" if not np.isnan(dt_auc_scores[-1]) else "AUC: N/A")
    print(f"    Precision: {dt_precision_scores[-1]:.4f}")
    print(f"    Recall: {dt_recall_scores[-1]:.4f}")
    print(f"    F1-Score: {dt_f1_scores[-1]:.4f}")

print("--- Optimized Decision Tree Baseline: 10-Fold Cross-Validation Results (Mean ± Standard Deviation) ---")
print(f"Accuracy: {np.nanmean(dt_accuracy_scores):.4f} ± {np.nanstd(dt_accuracy_scores):.4f}")
print(f"AUC: {np.nanmean(dt_auc_scores):.4f} ± {np.nanstd(dt_auc_scores):.4f}")
print(f"Precision: {np.nanmean(dt_precision_scores):.4f} ± {np.nanstd(dt_precision_scores):.4f}")
print(f"Recall: {np.nanmean(dt_recall_scores):.4f} ± {np.nanstd(dt_recall_scores):.4f}")
print(f"F1-Score: {np.nanmean(dt_f1_scores):.4f} ± {np.nanstd(dt_f1_scores):.4f}")

def bootstrap_confidence_interval(data, alpha=0.95, n_bootstraps=10000):
    if len(data) == 0 or np.all(np.isnan(data)):
        return (np.nan, np.nan)

    data = np.array(data)[~np.isnan(data)]
    if len(data) == 0:
        return (np.nan, np.nan)

    bootstrap_means = []
    for _ in range(n_bootstraps):
        sample = np.random.choice(data, size=len(data), replace=True)
        bootstrap_means.append(np.mean(sample))

    lower_percentile = (1 - alpha) / 2 * 100
    upper_percentile = (1 + alpha) / 2 * 100

    lower_bound = np.percentile(bootstrap_means, lower_percentile)
    upper_bound = np.percentile(bootstrap_means, upper_percentile)

    return (lower_bound, upper_bound)

print("\n--- Optimized Decision Tree Baseline: 95% Confidence Intervals (from 10-Fold CV) ---")
ci_dt_accuracy = bootstrap_confidence_interval(dt_accuracy_scores)
ci_dt_auc = bootstrap_confidence_interval(dt_auc_scores)
ci_dt_precision = bootstrap_confidence_interval(dt_precision_scores)
ci_dt_recall = bootstrap_confidence_interval(dt_recall_scores)
ci_dt_f1 = bootstrap_confidence_interval(dt_f1_scores)

print(f"Accuracy CI: ({ci_dt_accuracy[0]:.4f}, {ci_dt_accuracy[1]:.4f})")
print(f"AUC CI: ({ci_dt_auc[0]:.4f}, {ci_dt_auc[1]:.4f})")
print(f"Precision CI: ({ci_dt_precision[0]:.4f}, {ci_dt_precision[1]:.4f})")
print(f"Recall CI: ({ci_dt_recall[0]:.4f}, {ci_dt_recall[1]:.4f})")
print(f"F1-Score CI: ({ci_dt_f1[0]:.4f}, {ci_dt_f1[1]:.4f})")